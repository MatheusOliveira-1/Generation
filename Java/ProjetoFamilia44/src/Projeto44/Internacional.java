package Projeto44;


public class Internacional {
	protected String nome;
	protected String estacao;
	protected int fuso;
	protected String moeda;
	protected float alimentacao;
	protected float passagem;
	protected float hospedagem;
	

	protected boolean visto;

	public Internacional(String nome, String estacao, int fuso, String moeda, boolean visto,
			float alimentacao, float passagem, float hospedagem) {
		this.hospedagem = hospedagem;
		this.passagem = passagem;
		this.alimentacao = alimentacao;
		this.nome = nome;
		this.estacao = estacao;
		this.fuso = fuso;
		this.moeda = moeda;
		this.visto = visto;
	}

	
	public float getAlimentacao() {
		return alimentacao;
	}

	public float getPassagem() {
		return passagem;
	}


	public float getHospedagem() {
		return hospedagem;
	}


	public void setHospedagem(float hospedagem) {
		this.hospedagem = hospedagem;
	}


	public void setPassagem(float passagem) {
		this.passagem = passagem;
	}


	public void setAlimentacao(float alimentacao) {
		this.alimentacao = alimentacao;
	}
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getFuso() {
		return fuso;
	}

	public void setFuso(int fuso) {
		this.fuso = fuso;
	}

	public String getEstacao() {
		return estacao;
	}

	public void setEstacao(String estacao) {
		this.estacao = estacao;
	}

	public String getMoeda() {
		return moeda;
	}

	public void setMoeda(String moeda) {
		this.moeda = moeda;
	}

	public boolean isVisto() {
		return visto;
	}

	public void setVisto(boolean visto) {
		this.visto = visto;
	}

}
